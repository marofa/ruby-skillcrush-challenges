today_mood = 'anxious'


case today_mood

when 'tired'
	puts "Take a nap."
when 'hungry'
	puts "Have a snack."
when 'thirsy'
	puts "Where's your mimosa?"
when 'anxious'
	puts "Take a chill pill."
when 'lonely'
	puts "Call a friend."
when 'excitd'
	puts "Scream and shout!"
else 
	puts "Guess you must be one with the universe."
end 